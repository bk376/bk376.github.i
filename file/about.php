                         <h2>What is Intel-Cornell Cup?</h2>
               <p>  Intel-Cornell Cup is a national embedded systems competition that unlike any other. It’s not about asking you solve the same problem every year nor is it a highly subjective contest for just the most expensive projects. It’s about elevating the projects you want to do to to prove their engineering value to the world and empowering you all to become the some of the strongest leading engineers and designers in the country. </p>
                         <h2>What you can design</h2>
                <p>
                    In the Intel-Cornell Cup, you can invent Anything You Want, as long as it features an Intel Atom in the end, but you have to define the challenge your invention is trying to solve. What is that need you’re trying to meet. 

                </p>
                <p>
                    And then take it one step further to say this is how you could measure the performance of any solution to that need, and there by I can prove to you that our design is one of the best if not THE best solution out there to meet that need. As an example of how this approach can truly make a difference in your designs

                </p>
                <p>
                             You are encouraged to use your current school projects as your entry. Whether its for a course, a senior design project, a masters of engineering project but you’re welcome to use that same exact project(just no MS or PhDs). But as a result of being a part of the Cup and developing your project against the competition’s professional design judging criteria

                </p>

                <h2>Ways to enter</h2>
                        
                <ol>
                
                    <li>Go onto the competition website. Write a 150 words and send in a photo with some contact info of your team’s 3-5 students and you’re a part of the competition. It’s that easy. We’ll then send you updates and more info including invitations to on-line Q&A sessions with top designer Dr. David R. Schneider.<br> Then at the end of the Fall semester
                    they can send an additional team to the Semi-Finals for every 10 teams registered. So register their teams. The more teams that register, the more support that we can justify to give to the Cup and students like you.  
                    </li>
                 <li>Another way to enter is by completing an application which is then blind reviewed, meaning we have no idea what college its from (we’ve said no to MIT and yes to a community college that actually did rather well). <br>But if its an awesome project, you’re into the Semi’s! So person has an equal chance to earn their way in and every single finalist could come from any school. Wouldn’t that be awesome!    The application is a little long though, so option 1 is still an option. 
                    </li>
                    </ol>
                         
                
                
